nume_dia = int(input())
if nume_dia == 1:
    print('Domingo')

elif nume_dia == 2:
    print('Segunda-feira')
elif nume_dia == 3:
    print('Terca-feira')
elif nume_dia == 4:
    print('Quarta-feira')
elif nume_dia == 5:
    print('Quinta-feira')
elif nume_dia == 6:
    print('Sexta-feira')
elif nume_dia == 7:
    print('Sabado')
else:
    print("Número inválido")